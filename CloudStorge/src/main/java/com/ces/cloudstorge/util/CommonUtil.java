    public static String format_fileSize(int size) {
        double valueTmp = size / 1.0;
        int step = 1024;
        double newData = 0;
        String unit = "";
        for (int i = 0; i < pow.length; i++) {
            if ((newData = (valueTmp * (Math.pow(step, pow[i] * -1)))) <= 1024) {
                unit = map.get(i);
                break;
            }
        }

        return String.format("%.2f%s", newData, unit);
    }

